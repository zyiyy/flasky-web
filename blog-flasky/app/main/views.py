# 设定show_followed cookie 的路由, 查询所有文章还是所关注用户的文章
@main.route('/all')
@login_required
def show_all():
    # cookie 只能在响应对象里设置, 使用make_response创建响应对象
    resp = make_response(redirect(url_for('.index')))
    # max_age 设置cookie的过期时间, 单位为s, 如果不指定参数max_age, 浏览器关闭后cookie就会过期
    # resp.set_cookie('show_followed', '', max_age=30 * 24 * 60 * 60)
    resp.set_cookie('show_followed', '')
    return resp


@main.route('/followed')
@login_required
def show_followed():
    resp = make_response(redirect(url_for('.index')))
    # resp.set_cookie('show_followed', '1', max_age=30 * 24 * 60 * 60)
    resp.set_cookie('show_followed', '1')
    return resp